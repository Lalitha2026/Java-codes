package com.tcs;

public class Loop_Concepts1 {
	
	public static void main(String[] args) {
		
		for (int i = 0; i < 2; i++) { //outer loop (i=i+1)
			for (int j = 0; j < 2; j++) { //inner loop
			System.out.println(i);	
			}
			
		}
		for (int i = 0; i < 2; i++) { //outer loop (i=i+1)
			for (int j = 0; j < 5; j++) { //inner loop
			System.out.println(i);	
			}
			
		}
	}
	

}
